package com.bridglabz.demo;

public class logicalOperators {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        System.out.println("a && b = " +(a && b));
        System.out.println("a || b = " +(a || b));
        System.out.println("!(a && b) = " +!(a && b));
    }
}
